	public static int noteNameToSemitoneNumber(String name) {
		String newName = name;
		if (name.length() == 2) {
			if (name.charAt(1) == 'b') {
				switch (name.charAt(0)) {
					case 'A':
						newName = "G#";
						break;
					case 'B':
						newName = "A#";
						break;
					case 'D':
						newName = "C#";
						break;
					case 'E':
						newName = "D#";
						break;
					case 'G':
						newName = "F#";
						break;
					//This shouldn't happen
					default:
						break;
				}
			}
		}
		int semitone = 0;
		switch (newName) {
			case "A":
				semitone = 1;
				break;
			case "A#":
				semitone = 2;
				break;
			case "B":
				semitone = 3;
				break;
			case "C":
				semitone = 4;
				break;
			case "C#":
				semitone = 5;
				break;
			case "D":
				semitone = 6;
				break;
			case "D#":
				semitone = 7;
				break;
			case "E":
				semitone = 8;
				break;
			case "F":
				semitone = 9;
				break;
			case "F#":
				semitone = 10;
				break;
			case "G":
				semitone = 11;
				break;
			case "G#":
				semitone = 12;
				break;
			default:
				semitone = 0;
		}
		return semitone;
	}

